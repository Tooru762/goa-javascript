// 1. ლუწია თუ კენტი
function evenOrOdd(number) {
    if (number % 2 === 0) {
        return "ლუწია";
    } else {
        return "კენტია";
    }
}

// გამოძახება
console.log(evenOrOdd(4));
console.log(evenOrOdd(7));
console.log(evenOrOdd(10));


// 2. რიცხვის კვადრატი
function square(number) {
    return number * number;
}

// გამოძახება
console.log(square(2));
console.log(square(5));
console.log(square(8));
